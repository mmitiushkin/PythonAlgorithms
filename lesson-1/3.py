# 3. По введенным пользователем координатам двух точек вывести уравнение прямой вида y=kx+b, проходящей через эти точки.
x1 = int(input('x1: '))
y1 = int(input('y1: '))
x2 = int(input('x1: '))
y2 = int(input('y1: '))
k = (y1 - y2) / (x1 - x2)
b = y2 - k * x2
if b != 0:
    print("y = %.1f*x + %.1f" % (k, b))
else:
    print("y = %.1fx" % k)